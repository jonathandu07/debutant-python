# Déclaration des variables
a = 12
b = 8

# Addition
addition = a + b
print("Addition:", addition)  # Résultat : 20

# Soustraction
soustraction = a - b
print("Soustraction:", soustraction)  # Résultat : 4

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)  # Résultat : 96

# Division
division = a / b
print("Division:", division)  # Résultat : 1.5

# Modulo
modulo = a % b
print("Modulo:", modulo)  # Résultat : 4