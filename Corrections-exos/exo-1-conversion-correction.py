# Demande à l'utilisateur d'entrer un nombre flottant
nombre_flottant = float(input("Entrez un nombre flottant : "))

# Conversion du nombre flottant en entier
nombre_entier = int(nombre_flottant)

# Affichage du résultat
print(f"Le nombre {nombre_flottant} converti en entier est : {nombre_entier}")