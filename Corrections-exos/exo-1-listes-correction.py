# Création de la liste
animaux = ["chat", "chien", "souris", "éléphant"]

# Accès au premier élément (indice 0)
premier_animal = animaux[0]
print("Premier animal :", premier_animal)  # Résultat : "chat"

# Accès au dernier élément (indice -1)
dernier_animal = animaux[-1]
print("Dernier animal :", dernier_animal)  # Résultat : "éléphant"