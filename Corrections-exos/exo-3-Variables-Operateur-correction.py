# -*- coding: utf-8 -*-
# Déclaration des variables
x = 15
y = 20

# Comparaison : x est-il plus grand que y ?
x_est_plus_grand = x > y
print("x est plus grand que y :", x_est_plus_grand)  # Résultat : False

# Comparaison : x est-il égal à y ?
x_est_egal = x == y
print("x est égal à y :", x_est_egal)  # Résultat : False

# Comparaison : x est-il différent de y ?
x_est_different = x != y
print("x est différent de y :", x_est_different)  # Résultat : True