# Demande à l'utilisateur d'entrer le premier nombre flottant
nombre1 = float(input("Entrez le premier nombre flottant : "))

# Demande à l'utilisateur d'entrer le deuxième nombre flottant
nombre2 = float(input("Entrez le deuxième nombre flottant : "))

# Calcul de la somme des deux nombres
somme = nombre1 + nombre2

# Affichage du résultat
print(f"La somme de {nombre1} et {nombre2} est : {somme}")