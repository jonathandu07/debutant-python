# Création de la liste vide
fruits = []

# Ajout de "pomme" à la fin de la liste en utilisant append()
fruits.append("pomme")

# Ajout de "banane" à la fin de la liste en utilisant append()
fruits.append("banane")

# Ajout de "cerise" à la fin de la liste en utilisant append()
fruits.append("cerise")

# Ajout de "orange" en deuxième position (indice 1) en utilisant insert()
fruits.insert(1, "orange")

# Affichage de la liste finale
print("Liste finale des fruits :", fruits)