# Déclaration des variables booléennes
a = True
b = False
c = True

# Expression : a and b
resultat_and = a and b
print("a and b :", resultat_and)  # Résultat : False

# Expression : a or b
resultat_or = a or b
print("a or b :", resultat_or)  # Résultat : True

# Expression : not c
resultat_not = not c
print("not c :", resultat_not)  # Résultat : False