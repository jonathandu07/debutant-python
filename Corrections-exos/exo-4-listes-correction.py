# Liste fruits créée précédemment
fruits = ['pomme', 'orange', 'banane', 'cerise']

# Suppression de "banane" en utilisant remove()
fruits.remove("banane")

# Suppression et affichage du dernier élément en utilisant pop()
dernier_fruit = fruits.pop()
print("Dernier fruit retiré :", dernier_fruit)

# Affichage de la liste restante
print("Liste restante des fruits :", fruits)