# Création de la chaîne alphabet
alphabet = "abcdefghijklmnopqrstuvwxyz"

# Accès à la première lettre
premiere_lettre = alphabet[0]
print("Première lettre :", premiere_lettre)

# Accès à la dernière lettre
derniere_lettre = alphabet[-1]
print("Dernière lettre :", derniere_lettre)

# Accès à la sous-chaîne de la 3ème à la 6ème lettre (indices 2 à 5)
sous_chaine = alphabet[2:6]
print("Sous-chaîne de la 3ème à la 6ème lettre :", sous_chaine)