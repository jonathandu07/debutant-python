# -*- coding: utf-8 -*-
# Déclaration de la variable
z = 10

# Ajouter 5 à z
z += 5
print("Après addition de 5 :", z)  # Résultat : 15

# Multiplier z par 2
z *= 2
print("Après multiplication par 2 :", z)  # Résultat : 30

# Soustraire 4 de z
z -= 4
print("Après soustraction de 4 :", z)  # Résultat : 26

# Diviser z par 3
z /= 3
print("Après division par 3 :", z)  # Résultat : 8.666666666666666 (ou arrondi 8.67 selon la précision)
