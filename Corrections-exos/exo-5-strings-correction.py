# Création de la chaîne phrase
phrase = "  Python est génial!  "

# Suppression des espaces au début et à la fin
phrase = phrase.strip()

# Conversion de toute la chaîne en majuscules
phrase = phrase.upper()

# Remplacement de "génial" par "puissant"
phrase = phrase.replace("GÉNIAL", "PUISSANT")

# Affichage de la chaîne modifiée
print("Chaîne modifiée :", phrase)