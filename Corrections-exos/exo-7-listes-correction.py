# Création de la liste mélange
melange = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]

# Utilisation de sorted() pour trier la liste
liste_triee = sorted(melange)

# Affichage de la liste triée
print("Liste triée :", liste_triee)

# Inversion de l'ordre des éléments de la liste originale en utilisant reverse()
melange.reverse()

# Affichage de la liste inversée
print("Liste originale inversée :", melange)