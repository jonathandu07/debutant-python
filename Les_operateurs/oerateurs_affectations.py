# Les opérateurs d'affectations

## L'affection simple
x = 3 # X vaut 3
print (x)

## L'affection et l'addition
x += 3 # x = x + 3
print(x)

## L'affection et la soustraction
x -= 3 # x = x - 3
print(x)


## L'affection et la multiplication
x *= 3 # x = X x  3
print(x)

## L'affection et la division
x /= 3 # x = X /  3
print(x)


## L'affection et la division entière
x //= 3 # x = X //  3
print(x)


## L'affection et le modulo
x %= 3 # x = X %  3
print(x)

## L'affection et la puissance
x **= 3 # x = X **  3
print(x)