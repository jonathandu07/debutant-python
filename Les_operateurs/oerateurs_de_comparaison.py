# Les opérateurs de comparaison

5 == 3 # Résultat : False (faux)
print("Est-ce que 5 est égale à 3 ?  : Résultat : ",5 == 3)

5 != 3 # Résultat : False (True)
print("Est-ce que 5 est différent de 3 ?  : Résultat : ",5 != 3)

5 > 3 # Résultat : False (True)
print("Est-ce que 5 est supérieur à 3 ?  : Résultat : ",5 > 3)

5 < 3 # Résultat : False (False)
print("Est-ce que 5 est inférieur à 3 ?  : Résultat : ",5 < 3)

5 >= 3 # Résultat : False (True)
print("Est-ce que 5 est supérieur  ou égal à 3 ?  : Résultat : ",5 >= 3)

5 <= 3 # Résultat : False (False)
print("Est-ce que 5 est inférieur ou égal à 3 ?  : Résultat : ",5 <= 3)