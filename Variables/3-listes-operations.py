# Concaténation
liste1 = [1, 2, 3]
liste2 = [4, 5, 6]
liste_concatenee = liste1 + liste2
print(liste_concatenee)

print("------------------------------------------")
# Répétition
liste_repete = [1, 2, 3] * 3
print(liste_repete)

print("------------------------------------------")
